# string slicing
my_name = "Timothy Unkert"

# print the first name to the console using slicing
print(my_name[:7:])

# print the last name to the console using slicing
print(my_name[8::])

# step over each letter and print to the console
print(my_name[::2])

# reverse the string using slicing and print to the console
print(my_name[::-1])

