# multidimensional lists
my_multi_list = [
    ["Tim", "Sarah", "Ben"],
    [1, 2, 3, 4, 5],
    [True, False]
]

# access a multidimensional list via the index
print(my_multi_list[0][0])  # will print 'Tim' to the console
print(my_multi_list[-1][-1])  # will print False to the console
print(my_multi_list[2][0])  # will print True to the console

