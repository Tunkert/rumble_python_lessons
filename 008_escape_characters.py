# use escape characters to print single or double quotes in a string
print("I said, \"Hi!\"")
print('I said, \'Hi!\'')

# use the \t to tab over a line
print("\tThis line is tabbed over.")

# use the \n to create a newline
print("This is one line.\nThis is another line.")

# use \\ to do a forward slash
print("This is a \\ slash!")