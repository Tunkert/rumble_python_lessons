# create two variables, num1 and num2
num1 = 3
num2 = 4

# add them and print to the console
print(num1 + num2)  # 7
print(type(num1 + num2))

# subtract them and print to the console
print(num1 - num2)  # -1
print(type(num1 - num2))

# multiply them and print to the console
print(num1 * num2)  # 12
print(type(num1 * num2))

# divide them and print to the console
print(num1 / num2)  # 0.75
print(type(num1 / num2))

# use integer division, aka floor division
print(num1 // num2)  # 0
print(type(num1 // num2))

# get the remainder of num2 divided by num1
print(num2 % num1)  # 1
print(type(num2 % num1))

# raise a number to a power
print(num1 ** num2)  # 3^4 = 81
print(type(num1 ** num2))

# order of operations - Python, like most programming languages follows this pattern.
print(3 * (4 + 5))  # not 17, but 27 because of PEMDAS
